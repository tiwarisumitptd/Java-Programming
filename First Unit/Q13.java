public class Q13 {
    public static void main(String[] args) {
        int i = 1; // Start from 1
        do {
            System.out.println("Count is: " + i);
            i++; // Increase the value of i
        } while (i <= 5); // Loop runs while i is less than or equal to 5

        System.out.println("Loop ended.");
    }
}
